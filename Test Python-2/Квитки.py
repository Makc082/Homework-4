# s= Сума квитків, t = квитки, x = сума перших 3-х цифер, y = сума других 3-х цифер
s = 0

for t in range(1, 1000000):

    a1 = t // 100000 % 10
    a2 = t // 10000 % 10
    a3 = t // 1000 % 10
    b1 = t // 100 % 10
    b2 = t // 10 % 10
    b3 = t % 10

    x = a1 + a2 + a3
    y = b1 + b2 + b3

    if x == y:
        s += 1
        print(f"Щасливий квиток: {t}")

print(f"Загальна кількість щасливих квитків: {s}")